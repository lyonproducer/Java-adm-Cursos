        @Override
    public void run() {
        Thread ct = Thread.currentThread();
        while (ct == h1) {
            calcula();
            
            if (hora>=12){
                Hora.setText(hora + ":" + minutos + ":" + segundos+ " pm");
                
            }else
                Hora.setText(hora + ":" + minutos + ":" + segundos+ " am");
            
            try {
                Thread.sleep(1000);
            } catch (InterruptedException e) {
            }
        }
    }

 public void calcula() {
        Calendar calendario = new GregorianCalendar();
        hora =calendario.get(calendario.HOUR_OF_DAY);
        minutos = calendario.get(calendario.MINUTE);
        segundos = calendario.get(calendario.SECOND);
    }
 
 public void iniciarFechaHora(){
     
        Date fechaActual = new Date();
        DateFormat formatoHora = new SimpleDateFormat("HH:mm:ss");
        DateFormat formatoFecha = new SimpleDateFormat("dd/MM/yyyy");
        Hora.setText(formatoHora.format(fechaActual));
        Calendar.setText(formatoFecha.format(fechaActual));
     
 }
   
